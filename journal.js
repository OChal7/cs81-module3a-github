 // Greet a user by name
function greet(name) {
  return "Hello, " + name + "!";
}

// Get word length 
function wordLength(word) {
    return word.length; 
}

// Capitalize entire word
function makeBig(word) {
    return word.toUpperCase();
}

// Is even 
function isEven(number) {
    return number % 2 === 0;
}

// Is positive
function isPositive(number) {
    return number > 0;
}
